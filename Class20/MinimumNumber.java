public class MinimumNumber {
    public int min(int num1, int num2) {
      //  return num1<num2?num1:num2;
        if (num1<num2) {
            return num1;
        }else {
            return num2;
        }
    }

    public double min(double num1, double num2) {

        if (num1<num2) {
            return num1;
        }else {
            return num2;
        }


}}

