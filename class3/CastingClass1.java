package class3;

public class CastingClass1 {
    public static void main(String[] args) {
        int age=12;
        long age1=age;
        System.out.println(age1);
        byte number=1;
        int number2=number;
        System.out.println(number2);
        float num2=15.6f;
        int num3;
        num3=(int)num2;
        System.out.println(num3);










    }
}
